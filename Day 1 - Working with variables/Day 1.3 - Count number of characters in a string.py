# Write a program that prints the number of characters in a user's name. You might need to google fora function that calculates the length of a string.

# Using the len()
# This is the most straight forward way. Here we use a library function named len(). The string is passed as parameter to the function and we get a count of characters in the screen.

str = input("Please input a name to calculate the number of characters: ")
print("The number of characters in your string is: ",len(str))